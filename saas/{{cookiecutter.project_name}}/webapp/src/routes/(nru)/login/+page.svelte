<script lang="ts">
	import { goto } from '$app/navigation';
	import { pb } from '$lib/pocketbase';
	import { Collections } from '$lib/pocketbase/types';
	import { Form, createForm, FormError, SubmitButton, Input } from '$lib/forms';
	import { z } from 'zod';
	import { currentEmail } from './+layout.svelte';

	const schema = z.object({
		email: z.string().email(),
		password: z.string()
	});

	const superform = createForm(
		schema,
		async ({ form }) => {
			const { data } = form;
			const u = pb.collection(Collections.Users);
			await u.authWithPassword(data.email, data.password);
			await goto('/my');
		},
		{ email: $currentEmail },
		{ taintedMessage: null }
	);
	const keys = schema.keyof().Enum;

	const { capture, restore, form } = superform;
	export const snapshot = { capture, restore };

	$: $currentEmail = $form.email;
</script>

<Form {superform}>
	<Input
		id="email"
		type="email"
		label="Your email"
		field={keys.email}
		placeholder="name@foundation.org"
		{superform}
	/>
	<Input
		id="password"
		type="password"
		label="Your password"
		field={keys.password}
		placeholder="•••••"
		{superform}
	/>
	<FormError />
	<div class="flex justify-end">
		<SubmitButton>Log in</SubmitButton>
	</div>
</Form>
