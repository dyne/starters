<script lang="ts">
	import * as Sheet from '@/components/ui/sheet';
	import type { ComponentProps } from 'svelte';
	import { cn } from '../utils';
	import { Button } from '@/components/ui/button';
	import { Separator } from '@/components/ui/separator';

	//

	type SheetSide = ComponentProps<Sheet.Content>['side'];

	interface Props {
		side?: SheetSide;
		title?: string | undefined;
		open: boolean;
		class?: string;
		contentClass?: string;
		trigger?: import('svelte').Snippet<[any]>;
		children?: import('svelte').Snippet;
		content?: import('svelte').Snippet<[any]>;
	}

	let {
		side = 'right',
		title = undefined,
		open: isOpen = $bindable(),
		class: className = '',
		contentClass = '',
		trigger,
		children,
		content
	}: Props = $props();

	//

	function close() {
		isOpen = false;
	}

	function open() {
		isOpen = true;
	}
</script>

<Sheet.Root bind:open={isOpen} portal="body">
	<Sheet.Trigger asChild let:builder>
		{#if trigger}{@render trigger({ builder, open })}{:else}
			<Button builders={[builder]} class="shrink-0" variant="outline">
				{@render children?.()}
			</Button>
		{/if}
	</Sheet.Trigger>

	<Sheet.Content
		side="right"
		class="flex {cn({ '!min-w-[300px] !max-w-none': side == 'right' })} flex-col px-0 {className}"
	>
		{#if title}
			<Sheet.Header class="px-6">
				<Sheet.Title>{title}</Sheet.Title>
				<Separator></Separator>
			</Sheet.Header>
		{/if}

		<div class="overflow-y-auto overflow-x-visible px-6 {contentClass}">
			{@render content?.({ close })}
		</div>
	</Sheet.Content>
</Sheet.Root>
